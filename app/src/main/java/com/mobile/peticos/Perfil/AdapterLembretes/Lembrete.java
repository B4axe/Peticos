package com.mobile.peticos.Perfil.AdapterLembretes;

public class Lembrete {

    private String title;
    private String time;


    public Lembrete(String title, String time) {
        this.title = title;
        this.time = time;

    }

    public String getTitle() {
        return title;
    }

    public String getTime() {
        return time;
    }

}
